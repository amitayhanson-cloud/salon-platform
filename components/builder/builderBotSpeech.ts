/**
 * Conversational copy for the Caleno builder bot (Hebrew, RTL).
 * Each step can have multiple lines typed sequentially.
 */

export function getBuilderBotLines(step: number): string[] {
  switch (step) {
    case 1:
      return [
        "היי! אני העוזר החכם של Caleno.",
        "ביחד נבנה לכם אתר, מערכת הזמנות וניהול עסק בצורה קלילה ומהנה.",
        "קודם כל… ספרו לי קצת על העסק שלכם: איך הוא נקרא ומה הסוג?",
      ];
    case 2:
      return [
        "בואו נבחר איך האתר ייראה בהתחלה.",
        "אפשר לראות תצוגה חיה לכל עיצוב — ואל דאגה, הכול ניתן לעריכה אחר כך בפאנל.",
      ];
    case 3:
      return [
        "מצוין! איך תרצו לקרוא לקישור? משהו קצר ונוח שתוכלו לשלוח ללקוחות ושיזכרו בקלות.",
      ];
    case 4:
      return [
        "מעולה. איפה העסק נמצא בעולם האמיתי? הזינו את שם המקום בדיוק כמו בגוגל מפות, ככה המפה והביקורות יתחברו מושלם.",
      ];
    case 5:
      return [
        "מעניין אותי, מה הכי חשוב לכם כרגע מהאתר? אפשר לסמן כמה מטרות, זו שיחה בינינו.",
      ];
    case 6:
      return [
        "איך לקוחות יוכלו להגיע אליכם? טלפון, וואטסאפ, אינסטגרם... מה שנוח לכם. אפשר למלא רק מה שרלוונטי.",
      ];
    case 7:
      return [
        "כמעט סיימנו: מתי העסק פתוח לתורים? הגדירו את הימים והשעות — תמיד אפשר לעדכן אחר כך בפאנל.",
      ];
    case 8:
      return [
        "מעולה! נשאר רק לבחור מנוי ולשלם בדף מאובטח.",
        "חשוב לדעת: בלי אישור תשלום מוצלח לא נשמרים האתר והניהול — ככה הכול נשאר הוגן וברור.",
      ];
    default:
      return ["בואו נמשיך."];
  }
}
